export function getApprovedCompanies() {
    return companies;
}

export function filterCompaniesBySector(sectorId) {
    return companies.filter(c =>
        c.sector_id === parseInt(sectorId)
    );
}

export function filterCompaniesByCategory(categoryId) {
    return companies.filter(c =>
        c.category_id &&
        (Array.isArray(c.category_id)
            ? c.category_id.includes(parseInt(categoryId))
            : c.category_id === parseInt(categoryId))
    );
}

export function filterCompaniesBySubCategory(subCategoryId) {
    return companies.filter(c =>
        c.sub_category_ids &&
        c.sub_category_ids.includes(parseInt(subCategoryId))
    );
}

export function searchCompanies(query) {
    const lowerQuery = query.toLowerCase();
    return companies.filter(c =>
        c.company_name && c.company_name.toLowerCase().includes(lowerQuery) ||
        (c.company_profile && c.company_profile.toLowerCase().includes(lowerQuery)) ||
        (c.company_competence && c.company_competence.toLowerCase().includes(lowerQuery))
    );
}
