// Helper functions
export function getCompanyById(id) {
    console.log("[getCompanyById] ID: ", id);
    console.log("[getCompanyById] Companies: ", companies);
    console.log("[getCompanyById] Company: ", companies.find(c => c.id === parseInt(id)));
    return companies.find(c => c.id === parseInt(id));
}

export function getApprovedCompanies() {
    return companies.filter(c => c.status === 'Approved');
}

export function filterCompaniesBySector(sectorId) {
    return companies.filter(c =>
        c.status === 'Approved' &&
        c.interested_sector_id === parseInt(sectorId)
    );
}

export function filterCompaniesByProduct(product) {
    return companies.filter(c =>
        c.status === 'Approved' &&
        c.products_to_be_displayed.some(p =>
            p.toLowerCase().includes(product.toLowerCase())
        )
    );
}

export function filterCompaniesByCertification(certification) {
    return companies.filter(c =>
        c.status === 'Approved' &&
        c.certification.toLowerCase().includes(certification.toLowerCase())
    );
}

export function searchCompanies(query) {
    const lowerQuery = query.toLowerCase();
    return companies.filter(c =>
        c.status === 'Approved' && (
            c.company_name.toLowerCase().includes(lowerQuery) ||
            c.company_profile.toLowerCase().includes(lowerQuery) ||
            c.products_to_be_displayed.some(p => p.toLowerCase().includes(lowerQuery)) ||
            c.company_competence.toLowerCase().includes(lowerQuery)
        )
    );
}

// Get all unique products
export function getAllProducts() {
    const productsSet = new Set();
    companies.forEach(company => {
        if (company.status === 'Approved') {
            company.products_to_be_displayed.forEach(product => {
                productsSet.add(product);
            });
        }
    });
    return Array.from(productsSet).sort();
}

// Get all unique certifications
export function getAllCertifications() {
    const certificationsSet = new Set();
    companies.forEach(company => {
        if (company.status === 'Approved' && company.certification) {
            const certs = company.certification.split(',').map(c => c.trim());
            certs.forEach(cert => certificationsSet.add(cert));
        }
    });
    return Array.from(certificationsSet).sort();
}
